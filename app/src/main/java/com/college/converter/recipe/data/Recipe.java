package com.college.converter.recipe.data;

public class Recipe {
        String recipeName;
        String recipePicture_medium;
        String ingredient;



public String getRecipeName() {
        return recipeName;
        }

public void setRecipeName(String recipeName) {
        this.recipeName=recipeName;
        }

public String getRecipePicture_medium() {
        return recipePicture_medium;
        }

public void setRecipePicture_medium(String recipePicture_medium) {
        this.recipePicture_medium = recipePicture_medium;
        }

public String getIngredient() {
        return ingredient;
        }

public void setIngredient(String ingredient) {
        this.ingredient = ingredient;
        }



        }
